    private void initWebcam() {
        Dimension size = WebcamResolution.QVGA.getSize();
        webcam = Webcam.getWebcams().get(0);
        webcam.setViewSize(size);
        panel = new WebcamPanel(webcam);
        panel.setPreferredSize(size);
        panel.setFPSDisplayed(true);
        jPanel2.add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 480, 350));
        executor.execute(this);
    }

    @Override
    public void run() {
        Result result = null;
        do {
            try {
                Thread.sleep(100);
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            BufferedImage image = null;
            if (webcam.isOpen()) {
                if ((image = webcam.getImage()) == null) {
                    continue;
                }
            }
            LuminanceSource source = new BufferedImageLuminanceSource(image);
            BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));
            try {
                result = new MultiFormatReader().decode(bitmap);
            } catch (Exception e) {

            }
            if (result != null) {
                System.out.println(result.getText());
                System.out.println(LoginJFrame._idAccountLogin);
                if (result.getText().equalsIgnoreCase(LoginJFrame._idAccountLogin)) {
                    this.setVisible(false);
                    if ((new LoginJFrame()._quyenAccount).equals("Quản lý")) {
                        new MainQuanLy().setVisible(true);
                        webcam.close();
                        return;
                    }
                    if ((new LoginJFrame()._quyenAccount).equals("Nhân viên")){
                        new MainNhanVien().setVisible(true);
                        webcam.close();
                        return;
                    }
                    if ((new LoginJFrame()._quyenAccount).equals("Nhân viên kho")){
                        new MainThuKho().setVisible(true);
                        webcam.close();
                        return;
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Xác thực thất bại");
                    new LoginJFrame().setVisible(true);
                    this.setVisible(false);
                    return;
                }
            }
        } while (true);
    }

