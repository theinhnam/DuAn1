    private void btnLocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocActionPerformed
        String tu = ((JTextField) dcTu.getDateEditor().getUiComponent()).getText();
        String den = ((JTextField) dcDen.getDateEditor().getUiComponent()).getText();
        String timeStart = " 00:00:00.000";
        String timeEnd = " 23:59:59.000";
        if (!tu.isEmpty()&&!den.isEmpty()) {
            tu = tu + timeStart;
            den = den  + timeEnd;
        }
        if (tu == null || den == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ngày");
            return;
        }

        model = (DefaultTableModel) tblThongKe.getModel();
        model.setRowCount(0);
        ArrayList<ThongKeView> lisTKNV = thongKeService.getAllThongKeNVByDate(tu, den, idNhanVien);
        if (lisTKNV.size() == 0) {
            return;
        }
        for (ThongKeView thongKeView : lisTKNV) {
            model.addRow(new Object[]{
                thongKeView.getTen(),
                thongKeView.getDungTich(),
                thongKeView.getSoLuongDaBan()
            });
        }
    }//GEN-LAST:event_btnLocActionPerformed


