    private void loadData() {
        model = (DefaultTableModel) tblThongKe.getModel();
        model.setRowCount(0);
        ArrayList<ThongKeView> listThongKe = thongKeService.getAllThongKeNV(idNhanVien);
        for (ThongKeView thongKeView : listThongKe) {
            model.addRow(new Object[]{
                thongKeView.getTen(),
                thongKeView.getDungTich(),
                thongKeView.getSoLuongDaBan(),});
        }
    }
