    private void btnHoanThanhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHoanThanhActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn hoàn thành?");
        if (hoi != JOptionPane.YES_OPTION) {
            return;
        }
        if (txtGhiChu.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ghi chú");
            return;
        }
        String ghiChu = txtGhiChu.getText();
        ArrayList<HoaDonNhapHangChiTietView> listHDCT = nhapHangServices.getHDCT(_idHoaDon);
        boolean check = true;
        for (int i = 0; i < listHDCT.size(); i++) {
            if (listHDCT.get(i).getSoLuong() != listHDCT.get(i).getSoLuongThucTe()) {
                check = false;
            }
        }

        if (check) {
            JOptionPane.showMessageDialog(this, nhapHangServices.updateHD(ghiChu, _idHoaDon, idNhanVien));
        } else {
            JOptionPane.showMessageDialog(this, nhapHangServices.updateHDFailed(ghiChu, _idHoaDon, idNhanVien));
        }
        nhapHangServices.updateSoLuongTon(listHDCT);
        loadData();
        model2.setRowCount(0);
    }//GEN-LAST:event_btnHoanThanhActionPerformed

    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xác nhận?");
        if (hoi != JOptionPane.YES_OPTION) {
            return;
        }
        String _tenSanPham = tblHoaDonChiTiet.getValueAt(indexHDCT, 0).toString();
        int _kichThuoc = Integer.parseInt(tblHoaDonChiTiet.getValueAt(indexHDCT, 1).toString());
        String idSanPham = nhapHangServices.getID(_tenSanPham, _kichThuoc);
        int soLuongThucTe = 0;
        if (txtSoLuongThucTe.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng");
            return;
        }
        try {
            soLuongThucTe = Integer.parseInt(txtSoLuongThucTe.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng là số");
            return;
        }

        JOptionPane.showMessageDialog(this, nhapHangServices.update(_idHoaDon, idSanPham, soLuongThucTe));

        loadData2();


    }//GEN-LAST:event_btnXacNhanActionPerformed

