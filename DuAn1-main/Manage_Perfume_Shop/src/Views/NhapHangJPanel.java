    private void jTabbedPane1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MouseClicked
        model3 = (DefaultTableModel) tblSoLuongTon.getModel();
        model3.setRowCount(0);
        ArrayList<SanPhamView> listSP = nhapHangServices.getSoLuongTon();
        for (SanPhamView sanPhamView : listSP) {
            model3.addRow(new Object[]{
                sanPhamView.getTenSanPham(),
                sanPhamView.getKichThuoc(),
                sanPhamView.getSoLuongTon()
            });
        }
        tblSoLuongTon.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                final Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                c.setBackground(Color.white);
                c.setForeground(Color.black);

                return c;
            }
        });
        for (int i = 0; i < listSP.size(); i++) {
            int rows = i;
            if (listSP.get(i).getSoLuongTon() < 20) {
                tblSoLuongTon.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
                    @Override
                    public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                        final Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                        if (row <= rows) {
                            c.setBackground(Color.red);
                        } else {
                            c.setBackground(Color.white);
                            c.setForeground(Color.black);
                        }
                        return c;
                    }
                });
            }
        }
    }//GEN-LAST:event_jTabbedPane1MouseClicked

