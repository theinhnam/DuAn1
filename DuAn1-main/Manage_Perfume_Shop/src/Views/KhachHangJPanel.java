    private String convertTrangThai(int trangThai) {
        if (trangThai == 0) {
            return "Khách hàng mới";
        }
        if (trangThai == 1) {
            return "Khách hàng tiềm năng";
        }
        return "Khách hàng thân thiết";
    }

