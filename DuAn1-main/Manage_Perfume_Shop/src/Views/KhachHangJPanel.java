    private void loadData(List<QLKhachHang> listQLKH) {
        model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        for (QLKhachHang qLKhachHang : listQLKH) {
            model.addRow(new Object[]{
                qLKhachHang.getHoTen(),
                qLKhachHang.getSoDienThoai(),
                qLKhachHang.getNgayThem(),
                qLKhachHang.getNgaySua(),
                convertTrangThai(qLKhachHang.getTrangThai())
            });
        }
    }

