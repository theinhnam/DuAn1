    private void loadData() {
        model=(DefaultTableModel) tblKhuyenMai.getModel();
        model.setRowCount(0);
        ArrayList list=khuyenMaiService.getList();
        for(KhuyenMai km : khuyenMaiService.getList()){
            model.addRow(new Object[]{km.getIdKhuyenMai(),km.getTenKhuyenMai(),km.getSoLanApDung(),km.getChietKhau(),this.getTinhTrang(km.getTinhTrang())});
        }
    }
    public String getTinhTrang(int tinhTrang){
        if(tinhTrang==0){
            return "Ngừng hoạt động";
        }return "Còn hoạt động";
    }

    private void loadTrangThai() {
        ArrayList<KhuyenMai> lstKM=khuyenMaiService.getList();
        for(int i=0;i<tblKhuyenMai.getRowCount();i++){
            int soLan=Integer.parseInt(tblKhuyenMai.getValueAt(i, 2).toString());
            if(soLan==0){
                int tinhTrang=0;
                KhuyenMai km=new KhuyenMai(lstKM.get(i).getIdKhuyenMai(), null, null, null, soLan, WIDTH, tinhTrang);
                khuyenMaiService.updateTrangThai(km);
            }
        }
    }
