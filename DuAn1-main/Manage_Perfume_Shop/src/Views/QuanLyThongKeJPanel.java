    public CategoryDataset createDataset(){
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        Calendar c = Calendar.getInstance();
        ArrayList<ThongKeView> listData = thongKeService.getDataBieuDo();
        for (ThongKeView thongKeView : listData) {
            dataset.addValue(thongKeView.getDoanhThu(), "Doanh Thu", thongKeView.getThang());
        }
        return dataset;
    }
    
