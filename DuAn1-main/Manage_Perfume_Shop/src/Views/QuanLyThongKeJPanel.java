    private void loadTableThongKeSoLuongTon() {
        modelSLT=(DefaultTableModel) tblThongKeSoLuongTon.getModel();
        modelSLT.setRowCount(0);
        ArrayList<ThongKeView> lst=thongKeService.getListSoLuongTon();
        for(ThongKeView tkv: lst){
            modelSLT.addRow(new Object[]{tkv.getTen(),tkv.getDungTich(),tkv.getSoLuongTrongKho(),tkv.getSoLuonTon()});
        }
    }
